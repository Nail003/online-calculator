export function add(a, b) {
    /**
     * Return the sum of two numbers
     */
    return a + b;
}

export function subtract(a, b) {
    /**
     * Return the difference of two numbers
     */
    return a - b;
}

export function multiply(a, b) {
    /**
     * Return the product of two numbers
     */
    return a * b;
}
